package org.krista.seabattle;

// Class to represent the coordinates for tiles
public class Coordinate {
    private int x;
    private int y;


    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public Coordinate(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
